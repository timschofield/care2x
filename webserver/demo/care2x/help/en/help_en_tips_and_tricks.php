	</p>
	<h2><img src="<?php echo $root_path; ?>/gui/img/common/default/frage.gif" alt="" width=15 height=15> FAST search by first name alone!</h2>
 	<p>
 		<span class="bold" >Note:</span> 
 		Type the first name, type space, then type *. 
 		</br>
 		<img src="<?php echo $root_path; ?>help/en/img/en_search_quickfirst.png" alt="" id="screenshot" width=474 height=121>
