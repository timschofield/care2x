	<h2><img src="<?php echo $root_path; ?>/gui/img/common/default/frage.gif" alt="" width=15 height=15> Searching by first and family name.</h2>
 	<p>
 		<span class="bold" >Step:</span> 
 		Type the first and family name separating them with a space. 
 		</br>
 		<img src="<?php echo $root_path; ?>help/en/img/en_search_firstlast.png" alt="" id="screenshot" width=474 height=121>
 		<br/>
 		<span class="bold" >Note:</span>
 		The abovemost notice applies. The first and second words will be considered as probable beginning of first and family names respectively. So, the above entry example would probably return a list that also contains the "Adamson Smithsonian", "Adams Smiths", "Adamhoe Smitheren", etc.
	</p>
