
 	<h1>Search :: Tips &amp; Tricks</h1>
 	<h2><img src="<?php echo $root_path; ?>/gui/img/common/default/frage.gif" alt="" width=15 height=15> Important to remember!</h2>
 	<p>
 		<span class="bold" >Note:</span> 
 		Any word that you enter will be considered as a probable beginning of the information.
 		<br/><br/>
 		So, if for example you entered "Adam", Care2x will search for all family names (or first names) that begin with "adam". A search result will probably contain names like "Adam", "Adamson", "Adamo", etc.
	</p>
	<h2><img src="<?php echo $root_path; ?>/gui/img/common/default/frage.gif" alt="" width=15 height=15> What is the fastest way to search for a person?</h2>
 	<p>
 		<span class="bold" >Step:</span> 
 		Type his full personal ID number (PID).
	</p>
	<h2><img src="<?php echo $root_path; ?>/gui/img/common/default/frage.gif" alt="" width=15 height=15> How to search by family name?</h2>
 	<p>
 		<span class="bold" >Step 1:</span>
 		Just enter the name or few beginning characters in the input.
 		<br/> 
 		<span class="bold" >Step 2:</span> 
 		Click the "Search" button.
 		<img src="<?php echo $root_path; ?>help/en/img/en_search_fname.png" alt="" id="screenshot" width=476 height=230>
	</p>
	<h2><img src="<?php echo $root_path; ?>/gui/img/common/default/frage.gif" alt="" width=15 height=15> Why include the first name in searching?</h2>
 	<p>
 		<span class="bold" >Note:</span> 
 		If the "Search for first names too" radio button is shown, you need to click it (activate) to force Care2x to search by both family and first names. Otherwise (if button is un-clicked), only the family names will be searched. 
		</br>
		<img src="<?php echo $root_path; ?>help/en/img/en_search_radio.png" alt="" id="screenshot" width=476 height=229>
	</p>
