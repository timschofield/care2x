<p>
<span class="bold" >Report 1:</span>
Show all patients that were enrolled into ARV. Also patients that stop ARV or die are included and
patients that are not eligible for ARV.
<br/><br/>
<span class="bold" >Report 2:</span>
Shows all patients start ARV in the selected month.
<br/><br/>
<span class="bold" >Note:</span>
<br/>
Patients started ARV are defined as those patients who are given the status "2. start ARV" inside the ARV visit form.
<br/><br/>
