<br/>
<br/>
<span class="bold" >Report 3:</span>
Shows the number of patients currently on ARV.
<br/><br/>
<span class="bold" >Report 4:</span>
Shows all patients currently on ARV in the selected month with their prescribed drugs.
<br/><br/>
<span class="bold" >Report 5:</span>
<br/>
Number of patients that stop ARV with their reason for stopping ARV.
</p>
